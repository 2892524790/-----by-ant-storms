#ifndef PID_H
#define PID_H
#include "stdint.h"
#include "kalman.h"
#define NULL 0

enum PID_MODE
{
  PID_POSITION,
  PID_DELTA,
};

typedef struct	pid_type_def
{
    uint8_t mode;
    //PID 三参数
    fp32 Kp;
    fp32 Ki;
    fp32 Kd;

    fp32 max_out;  //最大输出
    fp32 max_iout; //最大积分输出

    fp32 set;      //设定
    fp32 fdb;				//反馈

    fp32 out;				
    fp32 Pout;//比例
    fp32 Iout;//积分
    fp32 Dout;//微分
    fp32 Dbuf[3];  //微分项 0最新 1上一次 2上上次
    fp32 error[3]; //误差项 0最新 1上一次 2上上次
		
		fp32 proportion;        //占输出总比
		fp32 final_out;				 //最终输出到电机的设定值
		int8_t    out_sign;					 //输入时的正负标志


} pid_type_def;
/**
  * @brief          pid struct data init
  * @param[out]     pid: PID struct data point
  * @param[in]      mode: PID_POSITION: normal pid
  *                 PID_DELTA: delta pid
  * @param[in]      PID: 0: kp, 1: ki, 2:kd
  * @param[in]      max_out: pid max out
  * @param[in]      max_iout: pid max iout
  * @retval         none
  */
/**
  * @brief          pid struct data init
  * @param[out]     pid: PID结构数据指针
  * @param[in]      mode: PID_POSITION:普通PID
  *                 PID_DELTA: 差分PID
  * @param[in]      PID: 0: kp, 1: ki, 2:kd
  * @param[in]      max_out: pid最大输出
  * @param[in]      max_iout: pid最大积分输出
  * @retval         none
  */
extern void PID_init(	pid_type_def *pid,
							uint8_t mode,  
							fp32 max_out, 
							fp32 max_iout,
							fp32 kp,
							fp32 ki,
							fp32 kd);

/**
  * @brief          pid calculate 
  * @param[out]     pid: PID struct data point
  * @param[in]      ref: feedback data 
  * @param[in]      set: set point
  * @retval         pid out
  */
/**
  * @brief          pid计算
  * @param[out]     pid: PID结构数据指针
  * @param[in]      ref: 反馈数据
  * @param[in]      set: 设定值
  * @retval         pid输出
  */
extern fp32 PID_calc(pid_type_def *pid, fp32 ref, fp32 set,extKalman_t Kal_Gim,short speed);
extern fp32 PID_ca(pid_type_def *pid, fp32 ref, fp32 set);

/**
  * @brief          pid out clear
  * @param[out]     pid: PID struct data point
  * @retval         none
  */
/**
  * @brief          pid 输出清除
  * @param[out]     pid: PID结构数据指针
  * @retval         none
  */
extern void PID_clear(pid_type_def *pid);
extern void pid_reset(pid_type_def *pid, float p, float i, float d);
extern void pid_i_reset(pid_type_def *pid);

#endif
